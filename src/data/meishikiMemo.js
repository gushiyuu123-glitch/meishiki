/* ─────────────────────────────────────────
   Utilities
───────────────────────────────────────── */
const BASE_YEAR  = 1984;
const BASE_INDEX = 0;

function safeStr(v) { return (v ?? "").toString().trim(); }

function parseBirth(birthStr) {
  const s = safeStr(birthStr);
  if (!s || !/^\d{4}-\d{2}-\d{2}$/.test(s)) return null;
  const [y, m, d] = s.split("-").map(Number);
  if (!y || !m || !d) return null;
  return { y, m, d };
}

function yearForNenchu({ y, m, d }) {
  if (m < 2) return y - 1;
  if (m === 2 && d < 4) return y - 1;
  return y;
}

function isNearRisshun({ m, d }) {
  return m === 2 && (d === 3 || d === 4);
}

function mod(n, m) { return ((n % m) + m) % m; }

function nenChuFromYear(y) {
  const idx    = mod(BASE_INDEX + (y - BASE_YEAR), 60);
  const stem   = STEMS[idx % 10];
  const branch = BRANCHES[idx % 12];
  return { idx, stem, branch, kanji: `${stem}${branch}` };
}

function hash32(str) {
  let h = 2166136261;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

function pick(arr, seedInt, salt = 0) {
  if (!arr?.length) return "";
  return arr[(seedInt + salt) % arr.length];
}

/* ─────────────────────────────────────────
   Seed design
   命式（筋）= birth のみ（core / friction / growth / distance / words / time）
   空気（遊び）= birth + name + place（seal のみ）
───────────────────────────────────────── */
function seedBirth(fd)  { return safeStr(fd?.birth); }
function seedAir(fd)    { return `${safeStr(fd?.birth)}|${safeStr(fd?.name)}|${safeStr(fd?.place)}`; }

